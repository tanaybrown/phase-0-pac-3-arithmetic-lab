function add(a, b){
    return (a + b)
}
function subtract(a, b) {
    return (a - b)
}
function multiply (a, b) {
    return (a * b)
}
function divide (a, b) {
    return (a / b)
}
function increment(n) {
    n++
    return n
}
function decrement(n){
    n--
    return n
}
function makeInt(n) {
    return parseInt(n, 10)
}
function preserveDecimal(n) {
    console.log(parseFloat("2.222"))
    console.log(parseFloat('sldkjflksjf'))
    return parseFloat(n)
}
